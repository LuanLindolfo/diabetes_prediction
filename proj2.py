import os
import kagglehub
import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import pickle

# Pré-processamento e Métricas
from sklearn.model_selection import train_test_split
from sklearn.preprocessing import StandardScaler
from sklearn.metrics import accuracy_score, recall_score, precision_score, classification_report, ConfusionMatrixDisplay

# Algoritmos de Machine Learning
from sklearn.naive_bayes import GaussianNB
from sklearn.linear_model import LogisticRegression
from sklearn.neighbors import KNeighborsClassifier
from sklearn.tree import DecisionTreeClassifier
from sklearn.ensemble import RandomForestClassifier
from sklearn.svm import SVC

"""# 2. CARREGAMENTO E PRÉ-PROCESSAMENTO"""

print("⏳ Baixando o dataset diretamente do Kaggle...")

# Download automatizado da pasta do dataset
caminho_pasta = kagglehub.dataset_download("alexteboul/diabetes-health-indicators-dataset")
print(f"✅ Pasta baixada em: {caminho_pasta}")

# Apontando para o arquivo específico perfeitamente balanceado
caminho_arquivo = os.path.join(caminho_pasta, "diabetes_binary_5050split_health_indicators_BRFSS2015.csv")

print("\n⚙️ Carregando e preparando os dados no Pandas...")
# Carrega o dataset
df = pd.read_csv(caminho_arquivo)

# Separação entre Previsores (X) e Classe Alvo (Y)
X = df.drop(columns=['Diabetes_binary']).values
Y = df['Diabetes_binary'].values

# Padronização dos dados (Crucial para KNN, SVM e Regressão Logística)
scaler = StandardScaler()
X_padronizado = scaler.fit_transform(X)

# Divisão em Treino (80%) e Teste (20%)
X_treino, X_teste, Y_treino, Y_teste = train_test_split(
    X_padronizado, Y, test_size=0.20, random_state=42
)

print(f"Registros para treinamento: {X_treino.shape[0]}")
print(f"Registros para teste: {X_teste.shape[0]}")

"""# 3. TREINAMENTO E AVALIAÇÃO DOS MODELOS"""

print("\nIniciando treinamento e avaliação dos modelos...\n")

# Dicionário com os modelos a serem testados
modelos = {
    "Naïve Bayes": GaussianNB(),
    "Regressão Logística": LogisticRegression(max_iter=1000, random_state=42),
    "KNN": KNeighborsClassifier(n_neighbors=5, metric='minkowski', p=2),
    "Árvore de Decisão": DecisionTreeClassifier(criterion='entropy', random_state=42),
    "Random Forest": RandomForestClassifier(n_estimators=100, criterion='entropy', random_state=42),
    "SVM (Linear)": SVC(kernel='linear', random_state=42)
}

resultados = []
melhor_recall = 0
nome_melhor_modelo = ""
modelo_vencedor = None

# Loop para treinar e testar todos os modelos
for nome, modelo in modelos.items():
    # Treinamento
    modelo.fit(X_treino, Y_treino)

    # Previsão
    previsoes = modelo.predict(X_teste)

    # Coleta de Métricas
    acuracia = accuracy_score(Y_teste, previsoes)
    recall = recall_score(Y_teste, previsoes) # Principal métrica para área da saúde
    precisao = precision_score(Y_teste, previsoes)

    resultados.append({
        "Modelo": nome,
        "Acurácia": round(acuracia, 4),
        "Recall": round(recall, 4),
        "Precisão": round(precisao, 4)
    })

    # Identifica o melhor modelo baseado no Recall (capacidade de achar a doença)
    if recall > melhor_recall:
        melhor_recall = recall
        nome_melhor_modelo = nome
        modelo_vencedor = modelo

"""# 4. EXIBIÇÃO DOS RESULTADOS"""

# Cria um DataFrame para exibir o ranking de forma elegante
df_resultados = pd.DataFrame(resultados).sort_values(by="Recall", ascending=False).reset_index(drop=True)

print("🏆 RANKING DOS MODELOS (Ordenado por Recall):")
display(df_resultados) # Use print(df_resultados) se não estiver usando Jupyter/Colab

print(f"\n🥇 O modelo vencedor focado na detecção (Recall) foi: {nome_melhor_modelo}")

# Gera a Matriz de Confusão do modelo vencedor
fig, ax = plt.subplots(figsize=(6, 5))
ConfusionMatrixDisplay.from_estimator(
    modelo_vencedor,
    X_teste,
    Y_teste,
    display_labels=['Saudável (0)', 'Diabético (1)'],
    cmap='Blues',
    ax=ax
)
plt.title(f'Matriz de Confusão - {nome_melhor_modelo}')
plt.grid(False)
plt.show()

"""# 5. EMPACOTAMENTO PARA PRODUÇÃO (Deploy)"""

print(f"\n📦 Salvando o modelo '{nome_melhor_modelo}' e o Scaler para produção...")

# Criando o dicionário com tudo o que a aplicação web (Streamlit) vai precisar
pacote_producao = {
    'modelo': modelo_vencedor,
    'scaler': scaler,
    'features': list(df.drop(columns=['Diabetes_binary']).columns)
}

# Exportando para arquivo .pkl
nome_arquivo_pkl = 'modelo_diabetes_producao.pkl'
with open(nome_arquivo_pkl, 'wb') as arquivo:
    pickle.dump(pacote_producao, arquivo)

print(f"✅ Arquivo '{nome_arquivo_pkl}' gerado com sucesso!")
print("Fim do processamento. O modelo está pronto para ser integrado a uma interface gráfica.")
